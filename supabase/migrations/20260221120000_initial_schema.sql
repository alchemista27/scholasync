-- Inisialisasi Skema Database untuk Scholasync

-- 1. Membuat tabel 'schools' untuk menyimpan data profil sekolah
CREATE TABLE IF NOT EXISTS public.schools (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    name text NOT NULL,
    address text NULL,
    logo_url text NULL,
    latitude real NULL,
    longitude real NULL,
    phone_number text NULL,
    website text NULL
);

-- Memberikan komentar pada tabel dan kolom untuk kejelasan
COMMENT ON TABLE public.schools IS 'Menyimpan informasi profil sekolah.';
COMMENT ON COLUMN public.schools.name IS 'Nama lengkap sekolah.';
COMMENT ON COLUMN public.schools.address IS 'Alamat lengkap sekolah.';
COMMENT ON COLUMN public.schools.logo_url IS 'URL ke file logo sekolah di Supabase Storage.';
COMMENT ON COLUMN public.schools.latitude IS 'Koordinat lintang lokasi sekolah.';
COMMENT ON COLUMN public.schools.longitude IS 'Koordinat bujur lokasi sekolah.';
COMMENT ON COLUMN public.schools.phone_number IS 'Nomor telepon resmi sekolah.';
COMMENT ON COLUMN public.schools.website IS 'Alamat situs web resmi sekolah.';

-- 2. Membuat tabel 'employees' untuk menyimpan data guru dan karyawan
CREATE TABLE IF NOT EXISTS public.employees (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    school_id bigint NOT NULL REFERENCES public.schools(id) ON DELETE CASCADE,
    full_name text NOT NULL,
    nipy text NULL,
    birthplace text NULL,
    birthdate date NULL,
    email text NULL UNIQUE,
    whatsapp_number text NULL
);

-- Memberikan komentar pada tabel dan kolom
COMMENT ON TABLE public.employees IS 'Menyimpan data guru dan karyawan yang terhubung dengan sekolah.';
COMMENT ON COLUMN public.employees.school_id IS 'Kunci asing yang merujuk ke sekolah tempat karyawan bekerja.';
COMMENT ON COLUMN public.employees.full_name IS 'Nama lengkap karyawan.';
COMMENT ON COLUMN public.employees.nipy IS 'Nomor Induk Pegawai Yayasan (jika ada).';
COMMENT ON COLUMN public.employees.email IS 'Alamat email karyawan, harus unik.';

