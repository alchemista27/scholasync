-- 5. Mengatur Keamanan (Row Level Security - RLS)
-- Mengaktifkan RLS untuk tabel
ALTER TABLE public.schools ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.employees ENABLE ROW LEVEL SECURITY;

-- Menghapus kebijakan yang mungkin ada sebelumnya
DROP POLICY IF EXISTS "Public can read school information" ON public.schools;
DROP POLICY IF EXISTS "Authenticated users can manage employees" ON public.employees;
DROP POLICY IF EXISTS "Allow public read access to logos" ON storage.objects;
DROP POLICY IF EXISTS "Allow authenticated users to upload logos" ON storage.objects;
DROP POLICY IF EXISTS "Allow authenticated users to update logos" ON storage.objects;


-- Membuat kebijakan RLS
-- Kebijakan 1: Siapa saja boleh membaca informasi sekolah.
CREATE POLICY "Public can read school information"
ON public.schools
FOR SELECT USING (true);

-- Kebijakan 2: Pengguna yang terautentikasi (login) dapat melihat, menambah, mengubah, dan menghapus data karyawan.
-- Ini adalah kebijakan awal yang bisa diperketat nanti (misal: hanya admin).
CREATE POLICY "Authenticated users can manage employees"
ON public.employees
FOR ALL USING (auth.role() = 'authenticated')
WITH CHECK (auth.role() = 'authenticated');

-- Kebijakan 3: Mengizinkan siapa saja untuk melihat file di bucket 'school-logos'.
-- Ini penting agar logo dapat ditampilkan di web.
CREATE POLICY "Allow public read access to logos"
ON storage.objects
FOR SELECT USING (bucket_id = 'school-logos');

-- Kebijakan 4: Mengizinkan pengguna yang terautentikasi untuk mengupload, mengubah logo.
CREATE POLICY "Allow authenticated users to upload logos"
ON storage.objects
FOR INSERT WITH CHECK (bucket_id = 'school-logos' AND auth.role() = 'authenticated');

CREATE POLICY "Allow authenticated users to update logos"
ON storage.objects
FOR UPDATE WITH CHECK (bucket_id = 'school-logos' AND auth.role() = 'authenticated');
